                    "ig" => (isLast ? "ツィク" : "ツィグ") + longVowel,
                    "ir" => "ツィァ",
                    "o" => "ツォ" + longVowel,
                    "oo" => "ツォー",
                    "or" => "ツォァ",
                    "u" => "ツ" + longVowel,
                    "ur" => "ツァ",
                    "y" => "ツィ",
                    "A" => "ツェ" + longVowel,
                    "Ar" => "ツェァ" + longVowel,
                    "Au" => "ツォイ" + longVowel,
                    "O" => "ツォェ",
                    "Or" => "ツォェァ",
                    "U" => "ツィ",
                    "Ur" => "ツィァ",
                    _ => throw new ArgumentOutOfRangeException(nameof(word.vowel)),
                },
                _ => throw new ArgumentOutOfRangeException(nameof(word.consonant)),
            };
    }
